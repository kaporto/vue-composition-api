<script>
import { ref } from 'vue'

export default{
  setup(){
    const message = ref("Hello")
    const quantity = ref(1)

    const increment = () => quantity.value++
    const decrement = () => quantity.value--
    return {
      message,
      quantity,
      increment,
      decrement
    }
  }
}

</script>

<template>
  <h1>{{message}}</h1>
  <h2>{{ quantity}}</h2>
  <button class="button" @click="increment">+</button>
  <button class="button" @click="decrement">-</button>

</template>

